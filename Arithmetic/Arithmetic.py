# program to perform basic arithmetic operation between two numbers

# Taking first number as input from user
a = int(input("Enter the number1:"))

# Taking second number as input from user
b = int(input("Enter the number2:"))

# Performing and displaying addition
print("Addition:",a+b)

# Performing and displaying substration
print("Subtraction:",a-b)

# performing and displaying muliplication
print("Multiplication:",a*b)

# Performing and displaying division
print("Division:",a/b)

# performing and displaying modulus(remainders)
print("Modulus:",a%b)
